export enum PlayMode {
  /**
   * Play a single track once, then stop playing
   */
  single = 'single',
  /**
   * Play tracks at random repeatedly
   */
  random = 'random',
  /**
   * Play tracks in a loop
   */
  loop = 'loop',
  /**
   * Play a single track repeatedly
   */
  singleloop = 'singleloop'
}
